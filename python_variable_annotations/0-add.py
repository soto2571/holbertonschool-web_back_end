#!/usr/bin/env python3
"""
This module contain a function that return the sum of two float number
"""


def add(a: float, b: float) -> float:
    """
    This function will return the sum of two float numbers

    Parameters:
    a (float): First float number
    b (float): Second float number

    Returns:
    float: The sum of the two float number.
    """
    return a + b
